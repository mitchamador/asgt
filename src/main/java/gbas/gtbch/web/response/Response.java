package gbas.gtbch.web.response;

public class Response {

    /**
     * message
     */
    String message;

    public Response() {
    }

    public Response(String message) {
        this.message = message;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }
}
